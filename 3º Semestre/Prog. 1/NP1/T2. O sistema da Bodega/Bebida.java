class Bebida{
	String nomeBebida, teorAlcoolico, ml;
	double preco;
	int estoque;

	public Bebida(String nomeBebida, String teorA, String ml, double preco, int estoque){
		this.nomeBebida = nomeBebida;
		this.teorAlcoolico = teorA;
		this.ml = ml;
		this.preco = preco;
		this.estoque = estoque;
	}
}