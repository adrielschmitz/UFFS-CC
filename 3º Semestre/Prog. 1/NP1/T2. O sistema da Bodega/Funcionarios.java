class Funcionarios{
	String nome, cpf, endereco, telefone, cargo;
	double salario;

	public Funcionarios(String nome, String cargo, double salario, String cpf, String endereco, String telefone){
		this.nome = nome;
		this.cargo = cargo;
		this.salario = salario;
		this.cpf = cpf;
		this.endereco = endereco;
		this.telefone = telefone;
	}
}